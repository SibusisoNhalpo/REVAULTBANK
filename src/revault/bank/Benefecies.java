    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText().trim();
        String acc = txtAccNumber.getText().trim();
        String branch = txtBranchCode.getText().trim();
        String bank = txtBankName.getText().trim();
        String email = Session.getEmail();

        if (email == null) {
            JOptionPane.showMessageDialog(this, "Please login first.");
            return;
        }

        if (name.isEmpty() || acc.isEmpty() || branch.isEmpty() || bank.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        try (Connection conn = DatabaseConnection.getConnection()) {
            String sql = "INSERT INTO beneficiaries (user_email, name, account_number, branch_code, bank_name) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, email);
            ps.setString(2, name);
            ps.setString(3, acc);
            ps.setString(4, branch);
            ps.setString(5, bank);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Beneficiary added successfully!");
            clearFields();

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String acc = txtAccNumber.getText().trim();
        if (acc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter the account number to delete.");
            return;
        }

        try (Connection conn = DatabaseConnection.getConnection()) {
            PreparedStatement ps = conn.prepareStatement("DELETE FROM beneficiaries WHERE account_number = ?");
            ps.setString(1, acc);
            int rows = ps.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Beneficiary deleted successfully!");
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "No beneficiary found with that account number.");
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error deleting: " + e.getMessage());
        }
    }

