    private void withdrawButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawButtonActionPerformed
        // TODO add your handling code here:
        // Step 1: Check login
        String email = Session.getEmail();
        if (email == null) {
            JOptionPane.showMessageDialog(this, "You must log in first.");
            new login11().setVisible(true);
            dispose();
            return;
        }

        // Step 2: Get entered amount
        String amountText = amountField.getText().trim();
        if (amountText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter an amount to deposit.");
            return;
        }

        try {
            double withdrawAmount = Double.parseDouble(amountText);
            if (withdrawAmount <= 0) {
                JOptionPane.showMessageDialog(this, "Enter a valid amount greater than 0.");
                return;
            }

            try (Connection conn = DatabaseConnection.getConnection()) {
                String selectSQL = "SELECT balance FROM users WHERE email = ?";
                java.sql.PreparedStatement ps = conn.prepareStatement(selectSQL);
                ps.setString(1, email);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    double currentBalance = rs.getDouble("balance");
                    double newBalance = currentBalance -withdrawAmount;

                    String updateSQL = "UPDATE users SET balance = ? WHERE email = ?";
                    ps = conn.prepareStatement(updateSQL);
                    ps.setDouble(1, newBalance);
                    ps.setString(2, email);
                    ps.executeUpdate();

                    SwingUtilities.invokeLater(() -> {
                        new Withdrawamount(withdrawAmount, newBalance).setVisible(true);
                        dispose();
                    });
                } else {
                    JOptionPane.showMessageDialog(this, "Email not found in database.");
                }
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid amount format.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
         
        
    }//GEN-LAST:event_withdrawButtonActionPerformed

