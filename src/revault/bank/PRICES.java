    private void labelNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelNextMouseClicked
        // TODO add your handling code here:
         if (selectedAmount == 0 ) {
        JOptionPane.showMessageDialog(this, "Please select an amount and first.");
        return;
    }

    String email = Session.getEmail();
    if (email == null || email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please log in first.");
        new login11().setVisible(true);
        dispose();
        return;
    }

    try (Connection conn = DatabaseConnection.getConnection()) {
        // Get user balance
        String sql = "SELECT balance FROM users WHERE email = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, email);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            double balance = rs.getDouble("balance");

            if (balance >= selectedAmount) {
                double newBalance = balance - selectedAmount;

                // Update balance
                String updateSQL = "UPDATE users SET balance = ? WHERE email = ?";
                PreparedStatement updatePS = conn.prepareStatement(updateSQL);
                updatePS.setDouble(1, newBalance);
                updatePS.setString(2, email);
                updatePS.executeUpdate();

                // Generate random airtime PIN (10 digits)
                String airtimePin = generateRandomPin(10);

                JOptionPane.showMessageDialog(this, 
                    "Airtime purchase successful!\n" +
                    "Amount: R" + selectedAmount + "\n" +
                    "PIN: " + airtimePin + "\n\nThank you!"
                );

                // Optional: reset selection
                selectedAmount = 0;
                selectedLabel.setText("No airtime selected. ");
                

            } else {
                JOptionPane.showMessageDialog(this, "Insufficient balance for this purchase.");
            }

        } else {
            JOptionPane.showMessageDialog(this, "User not found.");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

}
    private String generateRandomPin(int length) {
    String digits = "0123456789";
    StringBuilder pin = new StringBuilder();
    java.util.Random random = new java.util.Random();

    for (int i = 0; i < length; i++) {
        pin.append(digits.charAt(random.nextInt(digits.length())));
    }
    return pin.toString();

      
    }//GEN-LAST:event_labelNextMouseClicked

